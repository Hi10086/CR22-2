HCURSOR CServerDlg::OnQueryDragIcon()
{
	return (HCURSOR) m_hIcon;
}

LRESULT CServerDlg::OnSocket(WPARAM wParam, LPARAM lParam)
{
  WORD wError = WSAGETSELECTERROR(lParam);
  WORD wEvent = WSAGETSELECTEVENT(lParam);
  SOCKET Socket = wParam;

  switch(wEvent)
  {
  case FD_ACCEPT:
    {
      sockaddr addr;
      int nLen = sizeof(addr);
      accept(Socket, &addr, &nLen);
    }
    break;
  case FD_READ:
    {
      char szBuff[MAXBYTE];
      recv(Socket, szBuff, sizeof(szBuff), 0);
      AfxMessageBox(szBuff);
    }
    break;
  case FD_CLOSE:
    closesocket(Socket);
    break;
  }
  return TRUE;
}
